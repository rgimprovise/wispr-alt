// ─── Signing config ──────────────────────────────────────────────────────────
//
// We sign BOTH debug AND release builds with the same persistent keystore so
// that downstream installs can be updated in place (Android refuses to update
// an installed app when the new APK is signed by a different key — the cause
// of "приложение не установлено" we hit earlier).
//
// Keystore source priority:
//   1. Env vars (CI):   ANDROID_KEYSTORE_BASE64 / _PASSWORD / _ALIAS / _KEY_PASSWORD
//   2. local.properties (developer machine): keystorePath / keystorePassword /
//      keyAlias / keyPassword
//   3. Fallback: standard ~/.android/debug.keystore (regenerated by AGP if
//      missing — useful for first-time local setup, but signature differs
//      between machines)
//
// In CI the workflow base64-decodes ANDROID_KEYSTORE_BASE64 to a file before
// gradle runs, then sets KEYSTORE_PATH to point at that file.

val localProps = Properties().apply {
    val f = rootProject.file("local.properties")
    if (f.exists()) f.inputStream().use { load(it) }
}

fun secret(envKey: String, propKey: String): String? =
    System.getenv(envKey) ?: localProps.getProperty(propKey)

val keystorePath = secret("KEYSTORE_PATH", "keystorePath")
val keystorePassword = secret("ANDROID_KEYSTORE_PASSWORD", "keystorePassword")
val keyAlias = secret("ANDROID_KEY_ALIAS", "keyAlias")
val keyPassword = secret("ANDROID_KEY_PASSWORD", "keyPassword")
val haveCustomKeystore = listOf(keystorePath, keystorePassword, keyAlias, keyPassword)
    .all { !it.isNullOrBlank() }

android {
    namespace = "com.wispralt.keyboard"
    compileSdk = 34

    defaultConfig {
        applicationId = "com.wispralt.keyboard"
        minSdk = 26
        targetSdk = 34
        versionCode = 6
        versionName = "0.6.0"

        // Backend URL baked in at compile time.
        val backendUrl = System.getenv("WISPR_BACKEND_URL")
            ?: "https://alrcvscribe.n8nrgimprovise.space"
        buildConfigField("String", "BACKEND_URL", "\"$backendUrl\"")
    }

    signingConfigs {
        if (haveCustomKeystore) {
            create("wispr") {
                storeFile = file(keystorePath!!)
                storePassword = keystorePassword
                this.keyAlias = keyAlias
                this.keyPassword = keyPassword
            }
        }
    }

    buildTypes {
        debug {
            if (haveCustomKeystore) {
                signingConfig = signingConfigs.getByName("wispr")
            }
        }
        release {
            isMinifyEnabled = false
            proguardFiles(
                getDefaultProguardFile("proguard-android-optimize.txt"),
                "proguard-rules.pro"
            )
            if (haveCustomKeystore) {
                signingConfig = signingConfigs.getByName("wispr")
            }
        }
    }

    compileOptions {
        sourceCompatibility = JavaVersion.VERSION_17
        targetCompatibility = JavaVersion.VERSION_17
    }

    kotlinOptions {
        jvmTarget = "17"
    }

    buildFeatures {
        buildConfig = true
        viewBinding = true
    }
}

